function toogleTheme(event) {
    event.preventDefault()
    const changeTheme = document.querySelector("#change-theme")
    changeTheme.nextElementSibling.classList.remove("js-active-load");
    setTimeout(() => {
        if (wrapper.classList.contains("yellow")) {
            wrapper.classList.remove("yellow");
            localStorage.setItem("theme", "none");
        } else {
            wrapper.classList.add("yellow");
            localStorage.setItem("theme", "yellow");
        }
        changeTheme.nextElementSibling.classList.add("js-active-load")
    }, 3000)
    
}
function getLocalStorage() {
    if (localStorage.getItem("theme") !== null) {
        let color = localStorage.getItem("theme");
        if (color === "yellow") {
            wrapper.classList.add("yellow");
        }
    }
}

